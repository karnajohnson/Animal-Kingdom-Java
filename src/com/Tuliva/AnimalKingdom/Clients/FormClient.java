    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        this._firstName = txtFirstName.getText();
        this._lastName = txtLastName.getText();
        //logging what the user entered
        Utilities.LogIt("The user entered for last name:" + _lastName +
            "\nThe user entered for first name: " + _firstName + "\n" +
            "The password is classified.\n",
            Utilities.MessageSeverity.INFORMATIONAL, true);
        
        txtFirstName.setEnabled(false);
        txtLastName.setEnabled(false);
        txtPassword.setEnabled(false);
        btnSubmit.setEnabled(false);
        
        lstAnimal.setEnabled(true);
    }//GEN-LAST:event_btnSubmitActionPerformed

