    private void lstAnimalValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstAnimalValueChanged
        switch (this.lstAnimal.getSelectedIndex()){
            case 0:
                lblAnimal.setIcon(new ImageIcon("Resources/daffyduck.jpg"));
                Utilities.LogIt("FormClient::The user chose a duck in the list box.\n",
                    Utilities.MessageSeverity.INFORMATIONAL, true);
                    break;
            case 1:
                lblAnimal.setIcon(new ImageIcon("Resources/trout.png"));
                Utilities.LogIt("FormClient::The user chose a trout in the list box.\n",
                    Utilities.MessageSeverity.INFORMATIONAL, true);
                    break;
            case 2:
                lblAnimal.setIcon(new ImageIcon("Resources/Human.jpg"));
                Utilities.LogIt("FormClient::The user chose a human in the list box.\n",
                    Utilities.MessageSeverity.INFORMATIONAL, true);
                    break;
            default:
                break;
        }
    }//GEN-LAST:event_lstAnimalValueChanged

